.macro initRiver(bmbase, mbase, rbase) {
	lda bgcolor
	sta $d021
	ldy #0
	sty rrunByte
	sty rrunCount
	sty brunByte
	sty brunCount
	sty mrunByte
	sty mrunCount
	sty row
	
	// load read vectors
	mov16 #bitmap : breadV
	mov16 #matrix : mreadV
	mov16 #colorram : rreadV

	// prepare bitmap decode
	mov16 #bmbase : wrV2
	lda #40
	sta column
	lda #8
	sta colbyte
col:	mov16 wrV2 : wrV
	mla320(row, wrV)
block:	rleNextByte(bitmap, breadV, brunCount, brunByte)
	lda brunByte
	sta (wrV),y
	inc16 wrV
	dec colbyte
	bne block
	lda #8
	sta colbyte
	inc row
	lda row
	cmp #25
	beq !+
	jmp col
!:	sty row
	add16 wrV2 : #8 : wrV2
	dec column
	beq !+
	jmp col
!:
	// prepare matrix decode
	mov16 #mbase : wrV2
	lda #40
	sta column
	ldy #0
	sty row
col2:	mov16 wrV2 : wrV
	mla40(row, wrV)
